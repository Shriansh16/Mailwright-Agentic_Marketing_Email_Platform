# --- Schemas for Template Versioning (Sprint 2) ---


class TemplateVersionBase(BaseModel):
    """
    Base schema for a template version, containing common fields.
    """

    template_id: str = Field(
        description="Identifier for the parent template (e.g., graph execution ID)."
    )
    version_id: str = Field(
        description="Unique identifier for this specific version of the template."
    )
    mjml_source: str = Field(description="The MJML source code for this version.")
    compiled_html: str = Field(description="The compiled HTML from the MJML source.")
    user_brief_snapshot: Optional[dict] = Field(
        default=None,
        description="A snapshot of the user brief that led to this version.",
    )
    image_assets: Optional[dict] = Field(
        default=None,
        description="Information about image assets used in this version (e.g., URLs, prompts).",
    )
    change_trigger: Optional[dict] = Field(
        default=None,
        description="Information about what triggered the creation of this version (e.g., initial generation, feedback round).",
    )
    is_approved: bool = Field(
        default=False,
        description="Indicates if this version has been approved by the user.",
    )


