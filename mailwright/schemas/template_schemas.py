class TemplateCreationRequest(BaseModel):
    """
    The request model for POST /templates endpoint.
    Wraps the user brief and any other client-side info.
    Supports both legacy (structured brief) and RAG (plain text) flows.
    """

    # --- Flow Control ---
    rag_flow: bool = Field(
        default=False,
        description="If true, initiates the RAG-based workflow with a plain text brief. If false, uses the legacy workflow with a structured user_brief.",
    )

    # --- Brief Inputs (conditionally required) ---
    user_brief: Optional[UserBriefSchema] = Field(
        default=None, description="The structured brief for the legacy generation flow."
    )
    plain_text_brief: Optional[str] = Field(
        default=None, description="The unstructured, plain text brief for the RAG flow."
    )

    # --- RAG Flow Options (Sprint 5) ---
    use_placeholders: bool = Field(
        default=True,
        description="If true, use the template with placeholder images. If false, use original media.",
    )

    # --- Other ---
    client_id: Optional[str] = Field(
        default=None, description="Optional client identifier."
    )  # As per plan_langgraph.md

    @model_validator(mode="after")
    def check_brief_based_on_flow(self) -> "TemplateCreationRequest":
        """Ensures that the correct brief type is provided based on the rag_flow flag."""
        is_rag = self.rag_flow
        has_plain_brief = self.plain_text_brief is not None
        has_user_brief = self.user_brief is not None

        if is_rag and not has_plain_brief:
            raise ValueError(
                "When rag_flow is true, 'plain_text_brief' must be provided."
            )
        if not is_rag and not has_user_brief:
            raise ValueError("When rag_flow is false, 'user_brief' must be provided.")
        if is_rag and has_user_brief:
            raise ValueError(
                "Ambiguous request: When rag_flow is true, only 'plain_text_brief' should be provided."
            )
        if not is_rag and has_plain_brief:
            raise ValueError(
                "Ambiguous request: When rag_flow is false, only 'user_brief' should be provided."
            )
        return self


